function cleanFantasyReply(text, personaName) {
  if (!text) return "";
  let r = text;
  if (personaName) {
    r = r.replace(new RegExp(`^${personaName}\\s*:\\s*`, "i"), "");
  }
  r = r.replace(/\*[^*]+\*/g, "");
  r = r.replace(/[*_~`#]+/g, "");
  r = r.replace(/\(([^)]*действ[^)]*)\)/gi, "");
  r = r.replace(/:\)|:\(|;\)|:D|<3|:P|xD|XD|:о|:О/gi, "");
  r = r.replace(/[\u{1F600}-\u{1F64F}\u{1F300}-\u{1F5FF}\u{1F680}-\u{1F6FF}\u{1F1E0}-\u{1F1FF}\u{2600}-\u{26FF}\u{2700}-\u{27BF}\u{1F900}-\u{1F9FF}\u{1FA00}-\u{1FA6F}\u{1FA70}-\u{1FAFF}]/gu, "");
  r = r.replace(/\s{2,}/g, " ");
  return r.trim();
}

