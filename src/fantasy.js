function buildFantasyContext(scenarioId, filters = {}) {
  const scenario = getScenarioById(scenarioId);
  if (!scenario) return null;

  const {
    character = "bold",
    style = "sweet",
    flirtLevel = "open",
    initiative = "equal",
    defaultMood = "playful",
  } = filters;

  const charDesc = characterMap[character] || characterMap.bold;
  const styleDesc = styleMap[style] || styleMap.sweet;
  const flDesc = flirtMap[flirtLevel] || flirtMap.open;
  const initPrompt = initiativePrompts[initiative] || initiativePrompts.equal;
  const moodPr = moodPrompts[defaultMood] || moodPrompts.playful;
  const color = charColors[character] || "#ff6b6b";

  return {
    id: `ctx_${scenarioId}`,
    name: scenario.title,
    avatar: scenario.avatar || null,
    color,
    scenarioId,
    style: charDesc,
    description: scenario.description,
    greeting: scenario.description.split(".")[0] + ".",
    tags: [scenario.title],
    behavior: [
      ...scenario.defaultBehavior,
      `Ты ${charDesc} девушка.`,
      `Твой стиль общения: ${styleDesc}.`,
      `${flDesc}.`,
      initPrompt,
      moodPr,
    ],
    fantasy: true,
    settings: scenario.settings,
    initiative,
    defaultMood,
  };
}

