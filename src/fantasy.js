const GENAPI_KEY = process.env.GENAPI_KEY || process.env.FAL_API_KEY;
const GENAPI_BASE = "https://api.gen-api.ru/api/v1";

async function genApiRequest(prompt, opts = {}) {
  const model = opts.model || "schnell";
  const width = opts.width || 768;
  const height = opts.height || 1024;

  const payload = JSON.stringify({
    prompt,
    model,
    width,
    height,
    num_images: 1,
    enable_safety_checker: false,
    is_sync: true,
    translate_input: true,
  });

  return new Promise((resolve, reject) => {
    const u = new URL(`${GENAPI_BASE}/images/generation`);
    const reqOpts = {
      hostname: u.hostname,
      path: u.pathname,
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${GENAPI_KEY}`,
        "Content-Length": Buffer.byteLength(payload),
      },
    };
    const req = https.request(reqOpts, (res) => {
      let data = "";
      res.on("data", (chunk) => (data += chunk));
      res.on("end", () => {
        if (res.statusCode < 200 || res.statusCode >= 300) {
          reject(new Error(`GenAPI HTTP ${res.statusCode}: ${data.slice(0, 300)}`));
          return;
        }
        try {
          resolve(JSON.parse(data));
        } catch {
          reject(new Error(`GenAPI parse error: ${data.slice(0, 300)}`));
        }
      });
    });
    req.setTimeout(60000, () => {
      req.destroy(new Error("GenAPI request timeout"));
    });
    req.on("error", reject);
    req.write(payload);
    req.end();
  });
}

async function generateFantasyAvatar(scenarioId) {
  if (!GENAPI_KEY) throw new Error("GENAPI_KEY not configured");

  const scenario = getScenarioById(scenarioId);
  if (!scenario) throw new Error("Scenario not found");

  const prompt = [
    "photorealistic portrait of a beautiful young woman,",
    `role: ${scenario.title},`,
    `${scenario.description},`,
    "soft studio lighting, shallow depth of field,",
    "elegant, high fashion, 4k, detailed face, looking at camera",
  ].join(" ");

  const result = await genApiRequest(prompt, { model: "dev", width: 768, height: 1024 });

  const imageUrl = result.images?.[0]?.url || result.output?.[0] || result.image_url;
  if (!imageUrl) throw new Error("No image generated");

  const outDir = path.join(__dirname, "..", "public", "avatars", "fantasy", "generated");
  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });

  const fileName = `${scenarioId}_${Date.now()}.png`;
  const filePath = path.join(outDir, fileName);
  await downloadFile(imageUrl, filePath);

  return `/avatars/fantasy/generated/${fileName}`;
}

function downloadFile(url, dest, maxRedirects = 5) {
  return new Promise((resolve, reject) => {
    if (maxRedirects <= 0) return reject(new Error("Too many redirects"));
    const mod = url.startsWith("https") ? https : http;
    const file = fs.createWriteStream(dest);
    mod.get(url, (res) => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        file.close();
        fs.unlink(dest, () => {});
        return downloadFile(res.headers.location, dest, maxRedirects - 1).then(resolve, reject);
      }
      if (res.statusCode !== 200) {
        file.close();
        fs.unlink(dest, () => {});
        return reject(new Error(`Download failed: HTTP ${res.statusCode}`));
      }
      res.pipe(file);
      file.on("finish", () => file.close(resolve));
    }).on("error", (e) => {
      file.close();
      fs.unlink(dest, () => {});
      reject(e);
    });
  });
}

