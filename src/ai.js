function getCurrentChapter(affectionLevel, arc) {
  if (!arc) return null;
  const range = arc.levelRange[1] - arc.levelRange[0] + 1;
  const progress = affectionLevel - arc.levelRange[0];
  const chapterIdx = Math.min(Math.floor(progress / range * arc.chapters.length), arc.chapters.length - 1);
  return { ...arc.chapters[chapterIdx], index: chapterIdx };
}

