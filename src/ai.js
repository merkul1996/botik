/* ── Morning/Evening greetings ── */
async function generateGreeting({ personaId, timeOfDay, affectionLevel }) {
  const persona = getPersonaById(personaId);
  if (!persona) return null;
  const time = timeOfDay === "morning" ? "утро" : "вечер";
  if (!client) {
    return timeOfDay === "morning"
      ? `Доброе утро! Это ${persona.name}, скучала по тебе...`
      : `Добрый вечер! ${persona.name} думает о тебе...`;
  }
  const lvl = affectionLevel || 1;
  try {
    const response = await client.chat.completions.create({
      model: openAiModel, temperature: 0.9, max_tokens: 120,
      messages: [
        { role: "system", content: [
          `Ты — ${persona.name}. ${persona.style}.`,
          (persona.behavior || []).join(" "),
          `Уровень отношений: ${lvl}/7. ${LEVEL_PROMPTS[lvl] || ""}`,
          "Пиши ТОЛЬКО на русском. 1-2 предложения. Без спецсимволов.",
        ].join("\n") },
        { role: "user", content: `Сейчас ${time}. Напиши короткое ${time === "утро" ? "утреннее" : "вечернее"} сообщение парню от себя. Будь естественной.` },
      ],
    });
    let r = response.choices?.[0]?.message?.content?.trim() || `Привет! Думаю о тебе...`;
    return cleanReply(r, persona.name) || "Привет! Думаю о тебе...";
  } catch (e) {
    log.error({ err: e }, "generateGreeting error");
    return persona.greeting;
  }
}

async function analyzeMoodFromMessage(message, currentMoodId, personaId, affectionLevel) {
  if (!client) {
    const lower = message.toLowerCase();
    if (/люб|обожа|обним|целу|скуча/i.test(lower)) return { mood: "loving", intensity: 70, reason: "Тёплые слова" };
    if (/красив|милая|прекрасн|нежн|солн/i.test(lower)) return { mood: "happy", intensity: 65, reason: "Комплимент" };
    if (/дура|тупая|надоел|уйди|отстань|бесиш/i.test(lower)) return { mood: "offended", intensity: 75, reason: "Грубость" };
    if (/привет|как дела|здравств/i.test(lower)) return { mood: "curious", intensity: 50, reason: "Начало разговора" };
    if (/скучно|зевает|неинтересно/i.test(lower)) return { mood: "bored", intensity: 55, reason: "Скука" };
    if (/прости|извини|не хотел/i.test(lower)) return { mood: "tender", intensity: 60, reason: "Извинение" };
    return null;
  }

  const persona = getPersonaById(personaId);
  const prompt = [
    `Ты анализируешь настроение девушки ${persona?.name || "персонажа"} (${persona?.style || ""}).`,
    `Текущее настроение: ${currentMoodId}. Уровень отношений: ${affectionLevel}/7.`,
    `Парень написал: "${sanitizeUserInput(message)}"`,
    "",
    "Определи, как это сообщение повлияло на настроение девушки.",
    "Доступные настроения: happy, flirty, tender, excited, calm, sad, jealous, offended, bored, loving, curious, shy, angry, miss, neutral",
    "",
    "Ответь ТОЛЬКО JSON:",
    '{"mood":"id_настроения","intensity":50,"reason":"краткая причина на русском"}',
    "",
    "intensity от 0 до 100. Если сообщение никак не влияет — верни null.",
    "Учитывай характер персонажа! Дерзкая не обидится на лёгкую шутку, а стеснительная покраснеет от комплимента.",
  ].join("\n");

  try {
    const response = await client.chat.completions.create({
      model: openAiModel, temperature: 0.7, max_tokens: 100,
      messages: [
        { role: "system", content: "Ты анализатор эмоций. Отвечай ТОЛЬКО JSON или null." },
        { role: "user", content: prompt },
      ],
    });
    let raw = response.choices?.[0]?.message?.content?.trim() || "";
    if (raw === "null" || !raw) return null;
    raw = raw.replace(/^```json?\s*\n?/i, "").replace(/\n?```\s*$/i, "").trim();
    const parsed = JSON.parse(raw);
    if (!parsed || !parsed.mood) return null;
    const VALID_MOODS = ["happy","flirty","tender","excited","calm","sad","jealous","offended","bored","loving","curious","shy","angry","miss","neutral"];
    if (!VALID_MOODS.includes(parsed.mood)) parsed.mood = "neutral";
    return { mood: parsed.mood, intensity: Math.min(100, Math.max(0, Number(parsed.intensity) || 50)), reason: String(parsed.reason || "") };
  } catch {
    return null;
  }
}

function detectSpecialMoment(message, affectionLevel) {
  const moments = [];
  if (/люблю тебя|я тебя люблю|влюбился|влюбилась в тебя/i.test(message) && affectionLevel >= 4) {
    moments.push({ type: "confession", data: { message: message.slice(0, 100) } });
  }
  if (/красив|прекрасн|великолепн|шикарн|обалде/i.test(message) && !moments.length) {
    moments.push({ type: "first_compliment", data: { text: message.slice(0, 100) } });
  }
  return moments;
}

async function extractFacts(message, reply, existingFacts) {
  if (!client) return [];
  try {
    const existing = existingFacts.length
      ? `Уже известные факты: ${existingFacts.join("; ")}`
      : "Фактов пока нет.";
    const response = await client.chat.completions.create({
      model: openAiModel,
      temperature: 0.3,
      max_tokens: 200,
      messages: [
        {
          role: "system",
          content:
            "Ты извлекаешь конкретные факты о пользователе из диалога. Возвращай ТОЛЬКО JSON массив строк с новыми фактами. Каждый факт — короткое утверждение (5-10 слов). Примеры: \"Работает программистом\", \"Любит кошек\", \"Живёт в Москве\", \"Зовут Алексей\". Не повторяй уже известные факты. Если новых фактов нет — верни пустой массив [].",
        },
        {
          role: "user",
          content: `${existing}\n\nСообщение пользователя: "${sanitizeUserInput(message)}"\nОтвет девушки: "${sanitizeUserInput(reply)}"\n\nИзвлеки новые факты (JSON массив):`,
        },
      ],
    });
    const text = response.choices?.[0]?.message?.content?.trim() || "[]";
    const match = text.match(/\[[\s\S]*\]/);
    if (!match) return [];
    const parsed = JSON.parse(match[0]);
    if (!Array.isArray(parsed)) return [];
    return parsed
      .filter((f) => typeof f === "string" && f.length > 2 && f.length < 100)
      .slice(0, 5);
  } catch (e) {
    log.error({ err: e }, "extractFacts error");
    return [];
  }
}

